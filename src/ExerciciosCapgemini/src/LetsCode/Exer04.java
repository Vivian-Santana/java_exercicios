package LetsCode;
//TABUADA DE MULTIPLICAÇÃO DO 1 AO 10.
public class Exer04 {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			for (int j = 1; j <= 10; j++) { // VARIAVEL J VARIA DE 1-10 PRA SÓ DEPOIS VAIRAR A I.
				System.out.println(j + "x" + i + "=" + j * i);
			}
		}

	}

}
